int ndata_for_z(int Z) {
    if (Z <= 2) return 200;
    if (Z <= 10) return 400;
    if (Z <= 18) return 600;
    if (Z <= 36) return 800;
    if (Z <= 54) return 1000;
    if (Z <= 86) return 1200;
    if (Z <= 94) return 1400;
    return 0;
}

double get_free_polarizability(int Z) {
    if (Z < 1 || Z >= MAX_Z) return 0.0;
    return frepol[Z];
}

double get_free_volume(int Z, const std::string& functional, double hf_fraction) {
    if (Z < 1 || Z > MAX_Z) return 0.0;

    // For elements with functional-specific data (Z=1..36, excluding K=19, Ca=20)
    bool has_specific = (Z < 19 || (Z > 20 && Z <= 36));

    if (has_specific && hf_fraction < 0.0) {
        // Use functional-specific table
        if (functional == "blyp") return frevol_blyp[Z];
        if (functional == "b3lyp") return frevol_b3lyp[Z];
        if (functional == "bhahlyp" || functional == "bhandhlyp" || functional == "bhandh") return frevol_bhahlyp[Z];
        if (functional == "camb3lyp" || functional == "cam-b3lyp") return frevol_camb3lyp[Z];
        if (functional == "pbe") return frevol_pbe[Z];
        if (functional == "pbe0") return frevol_pbe0[Z];
        if (functional == "lcwpbe" || functional == "lc-wpbe" || functional == "lrc-wpbe") return frevol_lcwpbe[Z];
        if (functional == "pw86pbe" || functional == "pw86") return frevol_pw86[Z];
        if (functional == "b97-1" || functional == "b971") return frevol_b971[Z];
    }

    // Determine the real HF fraction for interpolation
    double rchf = hf_fraction;
    if (rchf < 0.0) {
        // Derive HF fraction from functional name
        if (functional == "blyp" || functional == "pbe" || functional == "pw86pbe" || functional == "pw86")
            rchf = 0.0;
        else if (functional == "b3lyp")
            rchf = 0.20;
        else if (functional == "b97-1" || functional == "b971")
            rchf = 0.21;
        else if (functional == "pbe0" || functional == "lcwpbe" || functional == "lc-wpbe" || functional == "lrc-wpbe")
            rchf = 0.25;
        else if (functional == "camb3lyp" || functional == "cam-b3lyp")
            rchf = 0.20;
        else if (functional == "bhahlyp" || functional == "bhandhlyp" || functional == "bhandh")
            rchf = 0.50;
        else
            rchf = 0.0;  // default to pure GGA
    }

    // For Z > 10 or fallback: use LSDA/UGBS
    if (Z > 10) {
        return frevol0[Z];
    }

    // Pure GGA shortcut
    if (std::abs(rchf) < 1e-10) {
        return frevol1[0][Z];
    }

    // Interpolate between HF fractions for periods 1 and 2
    if (rchf < 0.25) {
        return frevol1[0][Z] + (frevol1[1][Z] - frevol1[0][Z]) / 0.25 * rchf;
    } else if (rchf < 0.50) {
        return frevol1[1][Z] + (frevol1[2][Z] - frevol1[1][Z]) / 0.25 * (rchf - 0.25);
    } else if (rchf < 0.75) {
        return frevol1[2][Z] + (frevol1[3][Z] - frevol1[2][Z]) / 0.25 * (rchf - 0.50);
    } else {
        return frevol1[3][Z] + (frevol1[4][Z] - frevol1[3][Z]) / 0.25 * (rchf - 0.75);
    }
}

